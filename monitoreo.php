            Factor de Potencia
        </div>
        <p class="text-justify">
            Nombre dado a la relación de la potencia activa usada en un circuito, expresada en watts(KW), a la potencia aparente que se obtiene de las líneas de alimentación, expresada en kilovolts-amperes (KVA).
        </p>
        <p class="text-justify">
            Motores, transformadores, lámparas fluorescentes, equipos de refrigeración y otros similares necesitan de corrientes reactivas para su operación, la cual no produce un trabajo físico directo en los equipos, es necesaria para producir el flujo electromagnético que pone en funcionamiento.
        </p>
        <p class="text-justify">
            Cuando la cantidad de estos equipos es apreciable los requerimientos de potencia reactiva también se hacen significativos, lo cual produce una disminución del exagerada del factor de potencia.
        </p>
        <p class="text-justify">
            Un alto consumo de energía reactiva puede producir:
        </p>
        <ul>
            <li class="bullet_g">Aumento de la intensidad de corriente</li>
            <li class="bullet_g">Pérdidas en los conductores y fuertes caídas de tensión</li>
            <li class="bullet_g">Incrementos de potencia de las plantas, transformadores, reducción de su vida útil y reducción de la capacidad de conducción de los conductores</li>
            <li class="bullet_g">La temperatura de los conductores aumenta y esto disminuye la vida de su aislamiento</li>
            <li class="bullet_g">Aumentos en sus facturas por consumo de electricidad</li>
        </ul>
        <p class="text-justify">
            A la empresa distribuidora de energía:
        </p>
        <ul>
            <li class="bullet_g">Mayor inversión en los equipos de generación, ya que su capacidad en KVA debe ser mayor, para poder entregar esa energía reactiva adicional</li>
            <li class="bullet_g">Mayores capacidades en líneas de transmisión y distribución así como en transformadores para el transporte y transformación de esta energía reactiva</li>
            <li class="bullet_g">Elevadas caídas de tensión y baja regulación de voltaje, lo cual puede afectar la estabilidad de la red eléctrica</li>
        </ul>
