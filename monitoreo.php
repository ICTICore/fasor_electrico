        <p class="text-justify">Se sabe que las armónicas causan diversos problemas que regularmente ocasionan que la vida útil de los equipos se reduzca, principalmente debido a que las corrientes armónicas circulantes ocasionan un calentamiento excesivo en los equipos, lo que repercute en un incremento en las pérdidas eléctricas, adicionalmente, los voltajes armónicos impuestos en los equipos hacen que el aislamiento sea esforzado más de lo normal.</p>
        <p class="text-justify">Existen en el mercado diversas opciones para solucionar el problema de las armónicas, al menos mantenerlo dentro de límites recomendados como es el filtro de armónicos.</p>
